#create an intersection, Union, difference of sets

#set_c1
set_c1 = {10,20,30,40,50}
#set_c2
set_c2 = {30,40,50,60,70,80}

#printing union
print(set_c1.union(set_c2))
#printing intersection
print(set_c1.intersection(set_c2))
#printing difference of sets
print(set_c1.difference(set_c2))
print(set_c2.difference(set_c1))
